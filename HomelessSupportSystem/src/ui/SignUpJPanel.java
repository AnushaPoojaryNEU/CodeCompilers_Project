    private void signUpButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signUpButtonActionPerformed
        // TODO add your handling code here:
        ShelterMonitoringEnterprise ent = system.getShelterMonitoringEnterprise();
        UserAccount ua = ModelCreationUtil.parseHomelessIndividualAccount(
            this, usernameText.getText(),
            passwordText.getText(),
            nameText.getText(),
            ent.getAddress(),
            cityText.getText(),
            zipCodeText.getText(),
            ageText.getText(),
            "HOMELESS_INDIVIDUAL");
        if (ua == null)
            return;
        
        
        ent.getUserAccountDirectory().addUserAccount(ua);
        ent.addHomelessIndividual((HomelessIndividual)ua.getIndividual());
        JOptionPane.showMessageDialog(this, "Account created successfully.");
        clearFields();
        db.storeSystem(system);
        prevScreen();
    }//GEN-LAST:event_signUpButtonActionPerformed

